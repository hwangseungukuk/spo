              경우 스포라이브 운영팀의 판단 하에 해당 경기의 베팅을 취소시킬 수
              있습니다.
              <br />
              - 승부조작경기 등의 방지를 위하여 해당 경기에 베팅 되는 액수가
              아무 사유없이 급등하는 경우 스포라이브 운영팀의 판단 하에 베팅을
              취소시킬 수 있습니다. <br />
              - 짧은 시간 안에 이유 없이 배당률이 급변하는 경우 스포라이브
              운영팀의 판단 하에 베팅을 취소 시킬 수 있습니다. <br />
            </p>
