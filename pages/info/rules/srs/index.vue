              - 경기 예정 시간이 수시로 변경되어 정상적인 경기로 보기 어려운
              경우 스포라이브 운영팀의 판단 하에 해당 경기의 베팅을 취소시킬 수
              있습니다.
              <br />
              - 갑작스럽게 주요 선수들이 변경되어 배당률의 현저한 변경 사유가
